# Усі пункти у цій програмі йдуть по черзі. Варіант коду із вибором необхідного пунтку знаходиться у файлі math[optimized].py у гілці репозиторію на GitHub.

# 1. Квадрат числа.
print("=== Квадрат числа ===")
x = float(input("Введіть число: "))
print("Квадрат числа:", x ** 2)
print()  # порожній рядок для розділу

# 2. “Середнє трьох чисел”
print("=== Середнє арифметичне трьох чисел ===")
a = float(input("Введіть перше число: "))
b = float(input("Введіть друге число: "))
c = float(input("Введіть третє число: "))
avg = (a + b + c) / 3
print("Середнє арифметичне:", avg)
print()

# 3. “Перетворення хвилин у години”
print("=== Хвилини → години та хвилини ===")
minutes = int(input("Введіть кількість хвилин: "))
print(minutes // 60, "год", minutes % 60, "хв")
print()

# 4. “Розрахунок знижки”
print("=== Ціна зі знижкою ===")
price = float(input("Введіть ціну: "))
discount = float(input("Введіть знижку у %: "))
final_price = price * (1 - discount / 100)
print("Ціна зі знижкою:", final_price)
print()

# 5. “Остання цифра числа”
print("=== Остання цифра числа ===")
number = int(input("Введіть число: "))
last_digit = number % 10
print("Остання цифра:", last_digit)
print()

# 6. “Периметр прямокутника”
print("=== Периметр прямокутника ===")
length = float(input("Введіть довжину: "))
width = float(input("Введіть ширину: "))
perimeter = 2 * (length + width)
print("Периметр:", perimeter)
print()

# 7. Виведення числа в стовпчик
print("=== Вивід 4-значного числа в стовпчик ===")
num = int(input("Введіть 4-значне число: "))
x1 = num // 1000
x2 = (num % 1000) // 100
x3 = (num % 100) // 10
x4 = num % 10
print(x1)
print(x2)
print(x3)
print(x4)
